<script type="text/javascript">
    $(document).ready(function(){
        $("button#send-mail").click(function(){
            $('.loading').show();
            $.ajax({
                type: "POST",
                url: "/index.php/welcome/contact",
                data: $('form#form-send-mail').serialize(),
                success: function(result){
                    if(result == 1){
                        $('.loading').hide();
                        alert('Bạn đã gửi mail thành công.');
                        $('.error').html('');
                        $("#form-send-mail")[0].reset();
                        window.location.href = "<?php echo base_url() ?>";
                    }
                    else
                        $('.error').html(result);
                    $('.loading').hide();
                    $('body').removeClass('opacity-body');
                },
                error: function(){
                    alert("failure");
                    $('.loading').hide();
                }
            });
        });
    });
</script>